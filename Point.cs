using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public struct Point
{
    //set point axis x and y
    public int X { get; set; }
    public int Y { get; set; }

    public Point (int x, int y)
    {
        //X = x
        //Y = y
        this.X = x;
        this.Y = y;
    }


}
